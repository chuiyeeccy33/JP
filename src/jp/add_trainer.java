    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        if (txtid.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Please enter the Trainer ID", "Incomplete Trainer ID", JOptionPane.ERROR_MESSAGE);
            //set cursor to the textbox
            txtid.requestFocus();
        }
        else if (txtname.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Please enter the Trainer Name", "Incomplete Trainer Name", JOptionPane.ERROR_MESSAGE);
            txtname.requestFocus();
        }
        else if(txtage.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "You must provide the age", "Incomplete Trainer Age", JOptionPane.ERROR_MESSAGE);
            txtage.requestFocus();

        }
        else if(txtgender.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "You must provide the gender", "Incomplete Trainer Gender", JOptionPane.ERROR_MESSAGE);
            txtgender.requestFocus();

        }
        else if(txtdob.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "You must provide the date of birth", "Incomplete Trainer Birthday Date", JOptionPane.ERROR_MESSAGE);
            txtdob.requestFocus();

        }
        else if (txtusername.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "You must provide your username", "Incomplete Username", JOptionPane.ERROR_MESSAGE);
            txtusername.requestFocus();
        }
        else if(txtpassword.getPassword().equals("")){
            JOptionPane.showMessageDialog(rootPane, "You must provide your password", "Incomplete Password", JOptionPane.ERROR_MESSAGE);
            txtpassword.requestFocus();

        }
        else{
            try{
                String password = new String(txtpassword.getPassword());
                add_trainer1 std = new add_trainer1(txtid.getText(),txtname.getText(), txtage.getText(),txtgender.getText(),txtdob.getText(),txtusername.getText(),password);
                FileWriter myFile = new FileWriter("Profile.txt",true);
                PrintWriter pw = new PrintWriter(myFile);
                pw.println(std.getTrainerID()+ "," + std.getTrainerName() +  "," + std.getTrainerage()+ "," + std.getTrainergender()+ "," + std.getTrainerdob()+ "," + std.getTrainerusername()+ "," + std.getTrainerpassword()+ "," + "Trainer");

                pw.close();
                JOptionPane.showMessageDialog(rootPane, "Trainer details added", "Add Successful", JOptionPane.INFORMATION_MESSAGE);
                // After closing the previous message dialog, ask user whether
                // to continue adding
                int YesNo = JOptionPane.YES_OPTION;
                if (JOptionPane.showConfirmDialog (null, "Do you want to add another record?","Add New", YesNo) == JOptionPane.YES_OPTION){
                    clearFields();

                }
                else{
                    this.dispose();
                    new staff_button(username,idstaff).setVisible(true);

                }
            }
            catch (IOException ex){
                JOptionPane.showMessageDialog(rootPane, ex.toString());
            }

        }
    }//GEN-LAST:event_btnaddActionPerformed

    private void clearFields(){ 
    //clears the textfields
    
    txtid.setText("");
    txtname.setText("");
    txtage.setText("");
    txtgender.setText("");
    txtdob.setText("");
    txtusername.setText("");
    txtpassword.setText("");
    
    //set the cursor back to the ID textfield
    txtname.requestFocus();
}

    private boolean checkEmpty(){
    if(txtid.getText().equals("")||txtname.getText().equals("")||txtage.getText().equals("")||txtgender.getText().equals("")||txtdob.getText().equals("")||txtusername.equals("")||txtpassword.equals("")){
        return true;       
    }
    else{ return false;}
    
    }
