    private void cbxcourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxcourseActionPerformed
       String course = (String) cbxcourse.getSelectedItem();
        try{
            File file = new File("Course.txt");
            Scanner sc = new Scanner(file);
            //read data from the file

            String temp;
            boolean found = false;

            while(sc.hasNext() && !found /*the system will stop running while it found the correct username and password*/){
                temp = sc.nextLine(); //read a line of text from file
                String[] tempArr;
                tempArr = temp.split (",");
                
                if (course.equals(tempArr[0])) {
                    txtfee.setText(tempArr[1]);

                    //txtfee.setEditable(false);
                }
            }
        }catch(FileNotFoundException ex) {
            ex.toString();
        }
    }//GEN-LAST:event_cbxcourseActionPerformed

