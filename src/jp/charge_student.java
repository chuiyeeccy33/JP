    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        if (cbxstdid.getSelectedItem().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Please enter the Student ID", "Incomplete Student ID", JOptionPane.ERROR_MESSAGE);
            //set cursor to the textbox
            txtfee.requestFocus();
        }
        else if (cbxcourse.getSelectedItem().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Please enter the Student Course", "Incomplete Student Course", JOptionPane.ERROR_MESSAGE);
            txtfee.requestFocus();
        }
        else if (cbxtrainer.getSelectedItem().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Please enter the Trainer Name", "Incomplete Trainer Name", JOptionPane.ERROR_MESSAGE);
            txtfee.requestFocus();
        }
        else if(cbxmonth.getSelectedItem().equals("")){
            JOptionPane.showMessageDialog(rootPane, "You must provide the month", "Incomplete Month ", JOptionPane.ERROR_MESSAGE);
            txtfee.requestFocus();

        }
        else if(txtfee.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "You must provide the Fee", "Incomplete Student Fee", JOptionPane.ERROR_MESSAGE);
            txtfee.requestFocus();

        }
        else{
            try{
                String id = (String) cbxstdid.getSelectedItem();
                String course = (String) cbxcourse.getSelectedItem();
                String trainer = (String) cbxtrainer.getSelectedItem();
                String month = (String) cbxmonth.getSelectedItem();

                charge_student1 charge = new charge_student1(txtchargeid.getText(),id,course,trainer,month,txtfee.getText());
                FileWriter myFile = new FileWriter("Charges.txt",true);
                PrintWriter pw = new PrintWriter(myFile);
                pw.println(charge.getStudentchargeID() + "," + charge.getStudentID()+ "," + charge.getStudentcourse() + "," + charge.getStudenttrainer() +  "," + charge.getStudentmonth()+ "," + charge.getStudentfee() + "," + "Student");
                
                
                System.out.println(txtchargeid);
                System.out.println(id);
                System.out.println(course);
                System.out.println(trainer);
                System.out.println(month);
                System.out.println(txtfee);
                
                pw.close();
                JOptionPane.showMessageDialog(rootPane, "Student charges added", "Add Successful", JOptionPane.INFORMATION_MESSAGE);
                // After closing the previous message dialog, ask user whether
                // to continue adding
                int YesNo = JOptionPane.YES_OPTION;
                if (JOptionPane.showConfirmDialog (null, "Do you want to add another record?","Add New", YesNo) == JOptionPane.YES_OPTION){
                    clearFields();

                }
                else{
                    this.dispose();
                    new staff_button(username,idstaff).setVisible(true);

                }
            }
            catch (IOException ex){
                JOptionPane.showMessageDialog(rootPane, ex.toString());
            }

        }

    }//GEN-LAST:event_btnaddActionPerformed

